function soma(a, b) {
  return a + b;
}
function subtracao(a, b) {
  return a - b;
}
function multiplicacao(a, b) {
  return a * b;
}
function divisao(a, b) {
  return a / b;
}  
console.log(soma(1, 2)); // 3
console.log(subtracao(1, 2)); // -1    
console.log(multiplicacao(1, 2)); // 2
console.log(divisao(1, 2)); // 0.5        
